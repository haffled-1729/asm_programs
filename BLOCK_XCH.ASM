LIST2 DW 2001H, 2002H, 2003H, 2004H, 2005H
COUNT DB 05H
DATA ENDS

CODE SEGMENT
  ASSUME CS:CODE, DS:DATA
  START: MOV AX, DATA
         MOV DS, AX
         LEA SI, LIST1
         LEA DI, LIST2
	 MOV CL, COUNT
   BACK: MOV AX, [DI]
         XCHG [SI], AX
         MOV [DI], AX
	 INC SI
	 INC SI
	 INC DI
	 INC DI
	 LOOP BACK
	 HLT
CODE ENDS
         END START
